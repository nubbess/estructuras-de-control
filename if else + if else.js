const nota = 1000;

if (nota === 5){
    console.log("enhorabuena, has obtenido un sobresaliente");
} else if (nota === 4) {
    console.log("buen trabajo, pero podrías haberlo hecho mejor")
} else if (nota === 3) {
    console.log("has obtenido un suficiente")
} else if (nota === 2) {
    console.log("aprobaste por poquito")
} else if (nota === 1) {
    console.log("la proxima hace algo más")
} else {
    console.log("error, introduce una nota entre 1 y 5")
}